# String concatenation examples in Python

# Concatenating words into a sentence
x = "my"
y = "is"
z = "Ashu"
# Original output without spaces
print(x + y + z)  # Outputs "myisAshu"
# Improved output with spaces
print(x + " " + y + " " + z)  # Outputs "my is Ashu"

print("=======================================================")

# Concatenating numerical strings
a = "1000000000"
b = "0"
# Concatenating as strings
print(a + b)  # Outputs "10000000000"
# Adding as integers
print(int(a) + int(b))  # Outputs 1000000000

print("=======================================================")

# Concatenating a numeric string with a text string
c = "10"
d = "Ashu"
# Concatenating string with numeric appearance and text
print(c + d)  # Outputs "10Ashu"

